// Records which Claude Code version Orbit was built against, by probing the build machine's
// installed `claude --version`. The captured value is written to src/shared/built-against.ts,
// which the app imports so it can (a) display the version it was tested with and (b) warn the
// user before upgrading Claude Code past a version Orbit has never seen.
//
// Runs automatically via the `prebuild` / `predev` npm hooks. If `claude` can't be found, the
// existing captured value is preserved (so a CI box without Claude doesn't wipe it).
import { execFileSync } from 'node:child_process'
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const OUT = path.join(__dirname, '..', 'src', 'shared', 'built-against.ts')

const verOf = (s) => (s.match(/(\d+\.\d+\.\d+)/) ?? [])[1] ?? null

function probeClaudeVersion() {
  // Try a couple of resolution strategies; `claude` may be a shim on PATH or a .cmd on Windows.
  const candidates = process.platform === 'win32' ? ['claude.cmd', 'claude'] : ['claude']
  for (const bin of candidates) {
    try {
      const out = execFileSync(bin, ['--version'], { encoding: 'utf8', timeout: 15_000 })
      const v = verOf(out)
      if (v) return v
    } catch {
      /* try next candidate */
    }
  }
  return null
}

function existingVersion() {
  try {
    const src = fs.readFileSync(OUT, 'utf8')
    return (src.match(/BUILT_AGAINST_CLAUDE_VERSION\s*=\s*['"]([^'"]+)['"]/) ?? [])[1] ?? null
  } catch {
    return null
  }
}

const probed = probeClaudeVersion()
const version = probed ?? existingVersion()

if (!probed) {
  console.warn(
    `[capture-claude-version] could not read \`claude --version\`; keeping existing value: ${version ?? 'null'}`
  )
}

const body = `// AUTO-GENERATED by scripts/capture-claude-version.mjs — do not edit by hand.
// Captured from the build machine's installed Claude Code so the app can show which version
// it was built/tested against and warn before updating past it.
export const BUILT_AGAINST_CLAUDE_VERSION: string | null = ${
  version ? `'${version}'` : 'null'
}
export const BUILT_AGAINST_CAPTURED_AT = '${new Date().toISOString()}'
`

fs.writeFileSync(OUT, body)
console.log(`[capture-claude-version] built against Claude Code ${version ?? 'unknown'}`)
